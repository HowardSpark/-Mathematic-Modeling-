%% sum求和函数
% (1)如果是向量(无论是行向量还是列向量)，都是直接求和
E = [1 2 3]%行向量形式
sum(E)
E = [1;2;3]%列向量形式
sum(E)
% (2)如果是矩阵，则需要指明是按行还是按列求和
clc
E = [1,2;3,4;5,6]
a = sum(E)%默认按列求和
a = sum(E,1)%按列求和(得到一个行向量)
